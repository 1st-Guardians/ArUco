#!/bin/bash

DEVICE="/dev/video0"
SAVE_DIR="$HOME/photos"

mkdir -p "$SAVE_DIR"

echo "??? ????? ?????."

# ??? ??? 5? ?? ??
ffplay \
-loglevel error \
-f v4l2 \
-input_format mjpeg \
-video_size 960x720 \
-framerate 30 \
-window_title "5? ? ??" \
-autoexit \
-t 10 \
"$DEVICE" &

PREVIEW_PID=$!

# ??? ?????
for i in 5 4 3 2 1
do
    echo "$i? ? ??"
    sleep 1
done

# ???? ???? ???
wait "$PREVIEW_PID" 2>/dev/null

# ??? ??? ??? ????? ?? ??
sleep 1

FILENAME="$SAVE_DIR/photo_$(date +%Y%m%d_%H%M%S).jpg"

# ?? ? ? ??
ffmpeg \
-loglevel error \
-f v4l2 \
-input_format mjpeg \
-video_size 960x720 \
-framerate 30 \
-i "$DEVICE" \
-frames:v 1 \
-q:v 2 \
-y "$FILENAME"

echo "?? ?? ??!"
echo "$FILENAME"

# ??? ?? ??
xdg-open "$FILENAME" >/dev/null 2>&1 &
